<h2>Sua Origem</h2>
<p>
	<span class="capital-letter">S</span><span class="font-weight-bold">eu nome de nascimento:</span>
	Wilma Medeiros Machado; Filha do Mestre de obra Archimedes de Souza Machado e da Diarista Dilma Medeiros Machado. Avós
	maternos: Manoel Medeiros e Zulmira Gonçalves Medeiros. Nasceu aos dias 26 de outubro de 1963 em Duque de Caxias, tendo
	residido na rua Dez em Jardim Primavera - Estado do Rio de Janeiro, Brasil.
</p>

<h2>Valores Herdados</h2>
<p>
	Numa volta ao túnel do tempo; em 1970, aos sete anos de idade, o pai de Wilma, Srº Archuimedes, alugava imóveis nos
	fundos da casa em que moravam. Nesta ocasião especificamente havia um inquilino que dava muita atenção a Wilma que já
	não tinha a mãe presente em razão da separação de seus pais. Certo dia tendo o moço deixado de lhes dar atenção, Wilma
	experimentou o medo da perda de outra afeição, e repetindo palavras ouvidas de algum adulto disse para consigo: - “A
	mim me faz, a mim me paga!”. E correu a colocar um caco de vidro numa passagem habitual por onde deveria passar o tal
	moço, objeto de afeição, e naquele momento objeto também de raiva e medo. Porém, aconteceu o imprevisível. Wilma ouviu
	uma voz firme e paternal que lhe pareceu ecoar do céu, a lhe perguntar: - Então, Wilma, o que vai ser? O bem ou o
	mal!? Wilma, teve seus joelhos esmorecidos e involuntariamente tombados ao chão pela força do ARREPENDIMENTO, enquanto
	as lágrimas despencavam-lhes nas faces infantis escurecidas, mas, ali iluminadas pela lua e estrelas que pareciam tão
	pertinho. Wilma, pediu perdão à Deus, e correu para atirar para bem longe o caco de vidro. Assim, Wilma teve seu
	primeiro encontro com Deus, e sua primeira grande ESCOLHA. O que vai ser, Wilma? E Wilma escolheu o bem, Wilma
	escolheu o amor, AMOR de Deus.
</p>
<p>
	Além desta experiência o seu pai lhe ensinou valores tais como: falar a verdade. Dizia ele: “ – Mentira tem perna
	curta”. A sua mãe membro da Igreja Internacional da Graça de Deus – Missionário R.R Soares, muito trabalhadora,
	honesta e guerreira, falecida após travar uma luta contra um câncer na boca, não cansava de glorificar a Deus mesmo
	nos instantes mais extremos de dor; deixando como herança materna; a perseverança; o que fez avivar ainda mais a fé de
	Wilma Machado em Deus, e no Senhor Jesus Cristo pela ação amorosa do Espírito Santo.
</p>

<h2>Infância, Estudo e Trabalho</h2>
<p>
	Mais tarde com a separação de seus pais ela juntamente com seus irmãos Dircéa e Hermano chegou a passar um período com
	seus avós no bairro de Pilar – Duque de Caxias. Aos nove anos (1972) foi morar em Rocha Miranda com sua mãe que agora
	tinha formado nova família; e assim, Wilma, conheceu Marcelo (Profº Pulmão) seu irmão caçula por parte de mãe; e mais
	tarde por parte de seu pai, conheceu o seu irmão Aldo, portador da Síndrome de Down. Wilma têm portanto, quatro
	irmãos.
</p>
<p>
	Wilma, chegou a estudar no <b>Colégio Hélio Rangel, em Jardim Primavera</b> onde nasceu, porém, seu registro escolar
	data do ano de 1973, quando da idade de 10 anos, sua mãe a matriculou no
	<b>Colégio Leandro Maciel próximo de sua casa</b>; em Rocha Miranda, ali cursou da 1ª a 4ª série do ensino
	fundamental. No ano de 1978, prosseguiu seus estudos à tarde na escola Joaquim Nabuco, situado na rua Dona Mariana no
	bairro de Botafogo; escola que durante a noite era chamada de Escola México.
</p>
<p>
	Voltemos alguns anos antes...Em, 1974, seu irmão Hermano, tendo ido trabalhar numa fábrica de gesso pediu a vaga que
	ocupava como vendedor de jornal para Wilma, que então, naquele ano teve seu 1° ofício; o de vendedora de jornal. A
	princípio ficava na porta de uma padaria, mas como vendia todos os jornais, o dono da banca propôs a sua mãe que Wilma
	trabalhasse na Banca mesmo, e assim Wilma foi promovida pela primeira vez à maiores responsabilidades. Porém, certo
	dia, o dono da banca deixou lhe um pedido por ele escrito e instruiu que ela deveria entregar ao fornecedor para que
	este trouxesse o material da semana, mas, este chegando ao ver a letra do patrão de Wilma, exclamou: “ – Que letra é
	esta? Seu Miguel comeu as letras...” e não efetuou todos os pedidos, pois, não conseguira interpretar a caligrafia do
	Português, e tendo chegado este; a menina com 11 anos ao se ver repreendida pelo seu patrão que a responsabilizava de
	ânimo alterado pelo pedido que não fora completo, respondeu ainda que timidamente: “Mas, o senhor comeu as letras...”
	. E assim Wilma foi dispensada. Entretanto, vejamos os sinais de Deus. Este foi o primeiro contato de Wilma Machado
	com as diversas literaturas populares.
</p>
<p>
	Em 1978, Wilma acostumada a acompanhar sua mãe em seus trabalhos de Diarista, teve seu segundo emprego em Laranjeiras
	numa casa de família como doméstica. Estava com 15 anos e então, foi cursar sua 5ª série na escola Joaquim Nabuco que
	a noite era Escola México, na rua Dona Mariana em Botafogo.
</p>
<p>
	Nesta escola Wilma conheceu Maria do Socorro, a Socorro como a chamavam, também o Norberto o colega que tirava muito
	“As”. Ricardo, sua paixão da adolescência ocultada, que morava num prédio suntuoso na praia do Flamengo. E suas amigas
	para sempre; Geralda, Maria Aparecia e Jacy Ferreira que graças ao Facebook reencontrou nossa protagonista, e
	atualmente prosseguem se falando.
</p>
<p>
	Em 1980, Wilma aos 17 anos continuou seus estudos a noite numa escola também em Botafogo, cujo nome de dia e de noite
	denomina-se escola México. Cursava a 6ª série, quando a professora de português passou para a turma uma redação, cujo
	tema era livre e o tempo máximo a ser entregue era de 15 minutos. Wilma escreveu e entregou bem antes do tempo a sua
	redação e a professora deu lhe nota A; nota máxima considerada na época. Wilma, sentindo se alegre, orgulhosa; e
	surpresa consigo mesma, pois, escrevera sobre as dificuldades de um pai e seu filho que viviam no morro; sem na
	verdade ela mesma nunca de fato pisado num morro até esta altura de sua vida. E por isto, mostrou a Prof.ª de
	história, sua redação, e esta pediu para que Wilma, lhe emprestasse; e passados alguns dias, Wilma foi chamada a
	secretaria, e a diretora juntamente com esta professora entregou - lhes uma bolsa de estudo pela fundação Oswaldo
	Cruz, cuja proposta era ajudar a formar escritores. O plano de Deus na vida desta escritora estava sendo sinalizados
	pela 2ª vez; isto sem contar que tinha acesso aos livros já quando trabalhou como auxiliar de sua mãe.
</p>
<p>
	Embora fosse digno seu trabalho como doméstica em Laranjeiras, estudar com a Professora Eloá Barbuda, numa sala no
	Leblon. A, ainda, adolescente pobre e influenciada pela época de muito preconceito, sentiu-se inferior entre aquelas
	pessoas tão bem vestidas, a exibir suas joias sem ter que se fazer acompanhar por seguranças. Ainda se podia... Wilma,
	mesma tinha apenas um vestido amarelo e uma sandália, e seu complexo de inferioridade acabou sendo um obstáculo, sendo
	superado somente anos mais tarde, pela graça de Deus, por meio de muitos livros de autoajuda.
</p>
<p>
	Em 1981, Wilma trabalhou em outra residência na rua Buarque de Macedo, cuja, senhora de nome Robertina, a empregou
	como dama de Companhia. O salário era menor do que a metade do salário mínimo, entretanto, Wilma podia continuar
	estudando, e, ainda podia praticar tae kwandô na academia do mestre Kim, um coreano que estava divulgando a arte
	marcial no Brasil na Rua Voluntários da Pátria, Botafogo. Wilma chegou até a faixa de cor verde. Em 1982, Wilma
	concluiu seu 1º grau. Nesta época, Wilma tinha pouco contato com seus pais e irmãos, indo visita-los apenas de vez
	enquanto. Cada vez mais fortalecia seu gosto por ler e escrever e fez dos diversos livros encontrados em seus
	trabalhos nas minibibliotecas, seu alimento. Costumava ler até o avançar das horas, chegando a ler à luz de vela...
	Leu Victor Hugo, Agatha Christie, Camões, Anchieta, Machado de Assis e tantos outros que o espaço aqui não permite
	citar.
</p>
<p>
	Ainda entre 1982 a 1985, Wilma trabalhou em outra residência, cuja família havia vindo de Moçambique afugentada por
	uma revolta que houve entre os nativos na África; dizia a patroa portuguesa. (Outro sinal: Patrões portugueses de
	novo).
</p>
<p>
	O salário não era muito, porém tinha a vantagem de estudar e praticar sua defesa marcial na academia do mestre Kim,
	que praticava em casa-trabalho com o filho caçula da família; o Pedro que tinha 11 anos. Permaneceu ali até que lendo
	um jornal viu uma oportunidade; e assim Wilma, foi trabalhar confeccionando bijuterias em Copacabana. A mudança de
	emprego levou a nossa protagonista a ir morar num pensionato para moças em Botafogo mesmo, na rua Dona Mariana.
</p>
<p>
	Passados uns três meses Wilma foi demitida, pois encerrara o período de vendas extras para a empresa, e assim ela se
	viu desempregada, porém, não por muito tempo, pois, Marlene de origem paraibana, e uma das moças da pensão, a chamou
	para trabalhar como Promotora de Vendas na então, Mirabel. Este foi o primeiro emprego de Wilma com carteira assinada.
	Depois da Mirabel, Wilma trabalhou ainda na Yakult em 1985, depois nas Casas Chammas em 1986, até que...
</p>

<h2>Casamento</h2>
<p>
	Até que <i>em 1986</i>, nos dias 27 de dezembro na Igreja São de Sebastião – Bento Ribeiro, RJ, às 17:30 hs, num
	sábado chuvoso, o Monsenhor Félix, celebra a união de Wilma, com o paulista Jorge Antônio Galvão, passando esta
	<b>a chamar-se: Wilma Machado Galvão, indo residir na região de Guarulhos no Estado de São Paulo</b>. Na terra da
	garoa, onde gerou cinco
	<b>filhos; Rafael Machado Galvão, Renato Machado Galvão, Renan Machado Galvão e Paloma Machado Galvão</b>, não fosse
	entre Rafael e Renato um anjinho partir sem que ela o trouxesse a luz fazendo-a, chorar por algum tempo a sua
	lamentável perda. <b>Ainda na terra da garoa</b>, Wilma também estudou espiritismo e participou das obras voluntárias
	Kardecistas, isto enquanto residiu por lá. Tendo uma vez indagado na recepção a então companheira de serviços
	voluntários, se acaso não haveria música de adoração à Deus naquele lugar; pois ela via com certa estranheza essa
	ausência de louvores ao Criador num local de serviços a favor de necessitados onde tudo se falava de amor ao próximo.
	A interpelada lhe respondeu que ali só tinha instrumental mesmo.
</p>
<p>
	Na terra da garoa, Wilma, ainda trabalhou na Credial (1989), depois na Promovel (1989 – 1990), no Centro Espírita
	Nosso Lar Casas André Luiz (1992 – 1994) e no Mercado Irmãos Pereiras Ltda (1995). Cursou Telefonista, Cabeleireiro na
	Escola Nonaka passando a trabalhar em casa como Cabeleireira, porque em 28 de dezembro de 1997, foi que Deus concedeu
	Paloma, que nasceu portadora da Síndrome de Down requerendo um cuidado especialmente extra.
</p>
<p>
	Wilma, foi muito bem sucedida em sua profissão de cabeleireira. Tinha uma clientela paulistana fiel que era atendida
	de forma exclusiva, onde todo o ambiente era preparado para trazer bem estar. Uma de suas clientes chamada Kely,
	chegou a visita-la, quando ela retornou ao Rio de Janeiro. Wilma, hospedou Kely por alguns dias, e Kely despediu-se da
	Cabeleireira amiga com pedido de que se ela retornasse a morar em São Paulo avisasse para que ela voltasse a tratar
	seus cabelos com ela.
</p>

<h2>Divórcio</h2>
<p>
	Em 1999, o pai de Wilma havia falecido, então Wilma em acordo com seu esposo Jorge, decidiram vir morar no Rio, pois
	Wilma não queria estar tão distante da mãe no dia em que Deus a levasse. De volta ao RJ, chegou a trabalhar numa
	Cooperativa (2004).
</p>
<p>
	Wilma, deu continuidade ao tratamento de sua filha Paloma na Instituição Vicente Moreti, em Bangu. Lá tratou sua filha
	Paloma que recebeu alta ao completar 14 anos de idade; e também chegou a levar outros dois de seus filhos para
	tratamentos que durou apenas 02 anos.
</p>
<p>
	No mês de maio de 2000, Wilma se divorcia de Jorge Antônio Galvão, e esta fica com a guarda dos quatros filhos do
	casal. Seu ex-cônjuge, retorna a terra da garoa. Neste mesmo ano Wilma que havia retornado aos seus estudos, vem a
	conhecer a gaúcha Luana Beatriz, na sala de aula, e, Luana, a convida para ir a um Culto à Deus, na Igreja Ass. de
	Deus Ministério Real – Pastora Simone. Wilma, vai levando os seus filhos, e ali há louvores à Deus. Em meio aos
	louvores o apelo é feito, e Wilma, acolhe este convite a Jesus Cristo que passa a ser seu único e suficiente Senhor e
	Salvador de sua vida.
</p>
<p>
	<b>Tempo difíceis</b>: Divorciada com 04 filhos, e Silse, a cachorra da família para alimentar... <b>Só Jesus</b> na direção.
</p>
<p>
	Em 2004, trabalhando como cabeleireira a domicílio, e mais o tratamento de sua filha Paloma, e de dois de seus quatro
	filhos. Mesmo diante de muitas responsabilidades Wilma, não resistiu ao convite de seu pastor Castorino, e passou a
	participar de um grupo de intercessão. Sentindo-se muita grata à Deus, mas entendendo ser pouco perto do que Deus lhe
	concedera; a SALVAÇÃO, pediu à Deus, lhe concedesse ser <b>sua escritora</b>.
</p>
<p>
	Nesta ocasião congregava na Presbiteriana – Pastor Lael Viana de Alcântara, na rua Júlio César em Bangu. Disse ela: “–
	Pequenas mensagens, Deus. Quem sabe possa colocar no boletim da Igreja...”, (O Pr. Lael, incentivava os membros a
	escreverem para este boletim). No que <b>Deus</b>, de imediato lhe respondeu, que aquele espaço, <b>Ele</b> não lhe concedia,
	pois era para que o pastor completasse suas mensagens para a Igreja.
</p>
<p>
	Mas, o Senhor só faz coisas grandes e profundas; e não foi diferente com Wilma Machado. Vieram as músicas, uma
	história para videoclipe, uma peça teatral cantada, “A VOLTA DE JESUS”, uma coletânea de poemas, o PROJETO <b
		>“Um por todos! Todos por um”</b
	>; inclusive, chegou a apresentar em 23 de novembro de 2013 e ano de 2015, o musical “Pelas ruas do Rio” com apoio de
	muitos.
</p>

<h2>Novo Batismo</h2>
<p>
	Quando do batismo de Wilma, no Min. Real, no ano de 2000 ou 2001, este foi realizado com seu sobrenome de casada.
	Entretanto, Wilma quando ali recebida já havia dado entrada em seu divórcio. Em 02 de outubro de 2002, Wilma passou a
	assinar com seu sobrenome de solteira. Seu divórcio mesmo só foi oficializado sete anos mais tarde. Passando a assinar
	com seu sobrenome de solteira, então, agora congregando perto de casa, na <b>Igreja Metodista Ortodoxa</b> –
	Congregação Jd Bangu, situada no Condomínio Ayrton Senna – Jd Bangu, RJ, Wilma solicita ao
	<b>Dirigente Pastor Luiz Alfredo Gonçalves Lemos</b> - que a batizasse novamente.
</p>
<p>
	Este <b>segundo e definitivo batismo</b> como Wilma Medeiros Machado, se deu nos dias <b>06 de abril de 2007</b>. O
	batismo foi <b>em Jacarepaguá</b> com água da cachoeira que fora represada numa espécie de piscina; e fato curioso ocorreu
	ali, de que no momento do batismo quando as palavras foram pronunciadas foi observado pelo Diácono Meríssimo, uma cobra
	nadando em volta, e uma águia sobrevoando as cabeças segundo o irmão que auxiliava o pastor na ocasião; ele disse nada
	ter falado no momento para não assustar a batizada, mas que isto também era uma sinal de Deus. Este batismo para Wilma
	teve muita importância pois foi como ela foi resgatada de fato; como solteira, muito embora com o ouvir a palavra, Wilma
	compreendeu que o divórcio desagradava a Deus, e pediu perdão ao êx-esposo, e pediu que este considerasse retornar, mas
	durante o processo do divórcio que levou sete anos, ele por três vezes interpelado por ela, afirmou que não era desejo
	de seu coração a reconciliação.
</p>
<p>
	Então, tendo recebido o documento da separação definitiva, Wilma enviou - lhe, cópia para a casa de seu ex-cônjuge
	desejando que ele se casasse com a mulher que ele já se relacionava quatro meses depois dela ter pedido a separação.
</p>
<p>
	Assim Wilma Medeiros Machado, seguiu seu caminho cuidando de seus quatro filhos e de sua cachorra Silse. E, claro que
	com a ajuda de Deus, e por sua misericórdia compondo músicas que lhes vinham à mente quando menos esperava... E Wilma,
	escrevia...registrava...escrevia...
</p>

<h2>Novo Casamento</h2>
<p>
	Em 29 de Dezembro de 2012, na igreja Assembleia de Deus Min. Shekinah, igreja onde congrega, o Pres. Pastor Aldo da
	Silva Gomes Filho, celebrou o casamento de Wilma e Marcio Luiz Tinoco Leite; ele com 52 anos, e Wilma com 49 anos,
	passa a chamar-se: Wilma Medeiros Machado Leite. O casal a princípio foi residir numa rua atrás da rua Adelaide, 05,
	onde Wilma morou com seus filhos a mais de doze anos. Chama-se rua Montreal, nº 5. Atualmente, (Out/2019), residem na
	rua G. Via 1.
</p>

