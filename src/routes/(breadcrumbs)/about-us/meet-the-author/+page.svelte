<h2>Concluindo</h2>
<p>
	Wilma, acredita que toda pessoa separada para trabalhar para Deus com o uso da escrita, principalmente se for o caso
	de como ela descreve seu dom, que ocorre por inspiração; em todos os casos acredita ela, que esta pessoa tem o dever
	cristão de mastigar a Palavra de Deus diariamente, tornando-se um investigador audaz, a fim de coibir quaisquer
	possibilidades de passar em suas escritas a tão presente heresias em todas as épocas avistadas.
</p>
<p>
	De 2011 a 2018, Wilma Machado, escreveu e administrou o Projeto “Um por todos! Todos por um”. Autora e Coordenadora;
	este projeto assistiu espiritual e material a diversas famílias das proximidades do Condomínio Ayrton Senna, onde
	residiu durante mais de doze anos.
</p>
<p>
	Recentemente, Wilma Machado, com apoio de seu esposo e seus quatro filhos passou a dedicar-se a exposição de suas
	obras que ao logo dos anos vem escrevendo. Para isto, abriu uma empresa que se chama: ‘WMCIA” – Produção Musical
	Exclusivo - CNPJ: 34.982.692/0001-32, para funcionar como pessoa jurídica, e assim melhor expor suas obras musicais,
	escritos diversos nas diversas redes de comunicação. Seu estilo musical é o “popular cristão” ou como ela mesmo gosta
	de chamar “Popcristã” que entende por uma mistura da música essencialmente cristã com enlevo popular bem ao sabor da
	necessidade desta nova época em que estamos a viver. Em suas composições Wilma Machado traz deste o samba à bossa
	nova; o grito de um povo barulhento como é o caso de seus corinhos ao happ. Bem eclética, Wilma Machado nos revela ser
	uma caixinha de surpresa musical, já que suas composições segundo ela, derivam de sua intimidade com o Pai celestial,
	que sob o nome do Filho, usa-a pela misericórdia para falar à todos, e nem todos sentem, e vibram da mesma forma, por
	isto este toque tão diversificado, a fim de falar à toda alma sedenta de seu Criador, diz ela. Wilma Machado, vem
	fazendo seu registro no mundo através de seus escritos hora, com poemas, hora com Peças teatrais, Musicais, hora com o
	seu feedback “UM DEDINHO DE PROSA” hora Pelas ruas do Rio... a cantar de suas composições; como nos revela em seus
	vídeos clips no You Tube. “Pelas ruas do Rio...”, com uma das composições da autora cujo álbum vem sendo distribuído
	nas plataformas digitais pela ONErpm.<br /><br />

	Com vocês: Wilma Machado – uma cidadã brasileira, e seus escritos a serviço do Rei Jesus Cristo. <b>FIM!</b>
</p>
