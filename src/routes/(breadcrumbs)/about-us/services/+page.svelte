<h1>Oferecemos às Famílias Voluntárias Ofertantes</h1>
<h2 class="sub-title">"Cristo em casa" (Isaias 61:1–3)</h2>

<p>
	O Projeto “Um por todos! Todo por um” a princípio propõe em sua NOVA forma de anunciar o Evangelho do Senhor Jesus
	Cristo, resgatar princípios e valores; conduta de uma vida cristã vivenciados em <b>Atos 2:42–47</b>. E, na formação
	<i>SISTEMATIZADA</i>
	de Campos Missionários onde as Igrejas locais, agindo em “UNIDADE”, <b><i>ININTERRUPTA</i></b>, ou seja, sem cessar,
	caminhando COMUNIDADE disponibiliza homens e mulheres de Deus para:
</p>
<p class="text-center">
	<i>“Comunicai com os santos suas necessidades”</i><br />
	(Romanos 12:13).
</p>

<h3>Discipular</h3>
<p class="sub-title">
	<i>(Mateus 22:29 / 2 Timóteo 3:16 / Salmos 119:11)</i>
</p>
<p>
