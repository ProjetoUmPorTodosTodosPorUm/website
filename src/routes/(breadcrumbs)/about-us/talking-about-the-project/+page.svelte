		>(MARCOS 16: 15 – 16 / MATEUS 28:18 – 20 / JOÃO 3: 16 – 18 / OSÉIAS 4:6 / MATEUS 18:10 – 14 / MATEUS 4:4 / MATEUS
		25:34 – 36 / Efésios 2: 8 -10 / Tiago 2: 14 – 26 / 1 JOÃO 3: 16 – 18 / ATOS 2: 42 – 47)</i
	>.
</p>
<p>
