<h1>Como Família Voluntária Ofertante</h1>

<h2>Comunidade</h2>
<p>São três as formas de uma família vir se tornar “Família Voluntária Ofertante - Comunidade” neste Projeto:</p>
<p>
	<b>1º-</b> Ocorre quando um(a) <b>Voluntário(a) ADM</b> de uma Igreja vinculada ao Projeto dentro de um Campo
	Missionário, <b>cuja função</b> dele(a) na Igreja é Diácono/ Diaconisa ou Evangelista, porém, na COMUNIDADE
	denomina-se: <b>Coord. de Campo – Externo</b>, visitando as famílias em uma determinada rua;
	<b>vestindo a blusa do Projeto</b>
	cuja identificação avista-se na altura do ombro esquerdo,
	<b>(Consulte no site: Campo Missionário + Voluntários ADM)</b> inscreve um membro da família que representará toda esta
	família.
</p>
<p>
	<b>ATENÇÃO:</b> este Voluntário ADM da Igreja, deverá solicitar apenas, nome completo, data de nascimento, endereço e
	contato, <b>jamais, documento algum.</b>
	A família deverá escolher numa lista a sua oferta de alimento não perecível. Daí todo mês na <b>data oficial</b> de
	entrega no país, deverá esta família levar no Posto de Coleta designado. Em geral sempre terá um Posto de Coleta na
	comunidade, próximo da casa desta família. Cada país tem suas datas estabelecidas, e elas devem ser acatadas para o
	bom êxito da Obra missionária. E, claro que a família poderá levar <b>mais de um item alimentício</b>, e até outros
	tipos, porém deve cuidar de não faltar com a oferta por ela escolhida. Este é um trabalho de compromisso com o SENHOR,
	ok?
</p>
<p>
	<b>2º-</b> Ocorre quando uma família fica sabendo do Projeto, e, deseja participar, mas não tem <b>AINDA</b>, Campo
	Missionário onde mora; e, neste caso, esta família poderá <b>ofertar em monetário</b> na conta bancária postada no site
	da Ferramenta Missionária. (A Igreja solicitará nos Campos Missionários apenas alimentos, móveis, roupas e afins, mas isto
	não impede de que simpatizantes da Obra de qualquer parte do mundo ofertem em dinheiro).
</p>
<p>
	<b>3º-</b> Ocorre quando uma família ao saber do Projeto, e, desejando participar da Obra deste, verifica aqui neste
	site o link <a href="/#where-we-operate">“ONDE ATUAMOS”</a>, e descobre de que aonde reside <b>AINDA</b>, não há Campo
	Missionário formado; <b>porém</b>, desejando muito participar com <b>oferta em alimentos não perecíveis</b>; esta
	família não se incomoda de levar a oferta ao POSTO DE COLETA, cuja comunidade tenha Campo Missionário.
	<b>Como procede esta família, então?</b>
	Muito simples. Basta fazer contato aqui no site mesmo expressando a vontade de participar que um (a) Voluntário Adm,
	Coordenador(a) de Campo -Externo devidamente uniformizado fará contato para proceder esta inscrição que será numa
	pasta exclusiva para esta família fora do Campo. Estamos a falar da “Família Voluntária Ofertante-APOIO EXTERNO”.<br
	/><br />
</p>

<h2>Eclesiastíca</h2>
<p>Apenas uma é a forma de uma família se tornar “Família Voluntária Ofertante - Eclesiástica” neste Projeto:</p>
<p>
	Ser cristão, membro de uma Igreja inserida neste Projeto; e que, portanto, tem esta o “POSTO DE COLETA” (cesto de
	alimentos) em sua dependência. Logo, se este membro não foi inscrito na qualidade de <b
		>“Família Voluntária Ofertante - Eclesiástica”</b
	>
	representando todos de sua casa para <b>ofertar 01 item alimentício não perecível</b>, este membro pode, e deve buscar
	este irmão que na Igreja é o Diácono ou Diaconisa, responsável pelo social, e no Projeto, sua função como Voluntário
	ADM. é de <b>Coord. de Campo – Interno</b>.
</p>
<p>
	Havendo mais de um membro desta família <b>na Igreja</b>, apenas um deve ser inscrito. Poderá se ofertar além da
	oferta de compromisso; sendo esta oferta a mais, denominada; <b>oferta extra</b>.
</p>
<p>
	Este é um trabalho de compromisso no qual com apenas 01 item a família participa. Não é a quantidade, mas a qualidade
	e assiduidade de sua oferta.
	<b>Dê conforme gostaria de receber.</b>
</p>
<p>
	Acredite! Na forma de ofertar, na atenção a data e local de entrega muito se revela sobre o ofertante. Lembrem de que
	esta oferta é para os “pequeninos do Senhor”: O que fizer a estes, ao Senhor Jesus, o fará.
</p>

