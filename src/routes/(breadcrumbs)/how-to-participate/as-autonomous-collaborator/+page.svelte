<h1>Como Colaborador Autônomo</h1>
<p>
	Nesta “Rede de Pescar” de muitas pontas todos são convidados a participarem. Os Colaboradores Autônomos, quem são?
</p>
<p>
	São aquelas pessoas que tomando conhecimento deste Projeto, sente arder no peito uma vontade de fazer parte desta
	Obra, e oferece seus préstimos. Por exemplo: um(a) dentista, um psicólogo, nutricionista, um cabeleireiro e por aí
	vai... Tem uma profissão e a exerce como autônomo, e quer ser um Voluntário ADM, nesta Ferramenta Missionária
	ofertando 1, 2 ou 3 horas para benefício de seu próximo?<br />
	Seja bem vindo(a)!
</p>

<h2>Como Proceder</h2>
<p>
	Busque a Igreja responsável pelo Campo Missionário, o Coordenador 1, este (a) estará fazendo seu registro na
	Ferramenta como Voluntário ADM para que possa trabalhar no Projeto. Também poderá enviar um e-mail para a Equipe
	Missionária do Campo em que trabalha, ou mora. Depois é só aguardar o contato da Equipe agendando o dia para
	inscrição.
</p>
<p>
	Se quer cooperar e não sabe como vá a Igreja e busque o pastor, a <i>“Seara é grande e, poucos são os SEAREIROS”</i>.
</p>

