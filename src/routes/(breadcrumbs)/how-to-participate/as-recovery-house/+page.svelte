<h1>Como Casa de Recuperação</h1>
<p>
	Nesta “Rede de Pescar” de muitas pontas as “Casas de Recuperação” participam desta obra com a Igreja do Senhor Jesus
	Cristo, como sendo em parceria. Há inclusive, uma documentação especialmente elaborada para a inscrição desta
	participante. Basta que se ainda não procurada, mas desejando seus administradores participarem entrarem em contato
	com a Igreja que responde pelo Campo Missionário mais próximo da Casa que representam.
</p>
<p>
	Uma vez inscrita no Projeto “Um por todos! Todo por um” esta “Casa de Recuperação” estará concordando em receber das
	ofertas alimentícias, e tudo mais que da coleta no Campo Missionário advir; inclusive apoio monetário, <b
		>e, em contra partida, esta “Casa de Recuperação”</b
	>
	cooperará acolhendo
	<b>“Moradores de Rua”</b> ou algum familiar de <b>“Assistência Efetiva ou Extra”</b> pelo Projeto;
	<b>mas desde que</b> ela tenha suporte suficiente para isto.
</p>
<p>
	A “Casa de Recuperação” interessada deve agendar com o Coordenador Geral uma visita á sua Instituição para que este
	conheça de suas Obras, e proceda sua inscrição no Projeto. Nesta Ferramenta Miis. a transparência é o “Musk”.
</p>
<p>
	É uma parceria entre o Projeto “Um por todos! Todo por um” e Casas de Recuperação baseado no amor e cooperação mútua.
	Um não deve depender da ação da outro para beneficiar pessoas que precisarem de seus recursos. A parceria objetiva
	fortalecer a ambas, mas só na priorização das vidas, nesta prática do amor desinteressado é que a parceria será
	possível. Entendido isto, sejam bem vidas a parceria.
</p>

