<h3 class="no-margin-bottom">MISSÃO</h3>
<span class="sub-title no-text-indent">(Marcos 16:15-16)</span>
<p>
	Pregar o Evangelho, fazendo o <i>“IDE”</i> <u>de dentro para fora</u>; numa ação ininterrupta percorrendo ruas,
	bairros no Estado, País e Continente donde está sua <b>INSERÇÃO</b> ou <b>ARROLAMENTO</b>; e, assim trazendo consigo
	as diversas Denominações eclesiásticas encontradas no caminho, promovendo, e incentivando a virem segurar numa das
	pontas desta imensa <i>“Rede de Pescar”</i>; e, juntas como um grande <i>“Cordão de Três Dobras”</i> analisando os
	conflitos advinda de quaisquer ordens que esteja a afetar a família, a comunidade, a nação trazendo solução quando da
	aplicação da Palavra de Deus, do amor Dele; exemplificando Cristo, para Cristo, e, em Cristo a ininterrupta UNIDADE
	entre Igrejas. Em suma: Em UNIDADE, preparar os
	<i>“Eleitos”</i> para aquele grande dia. O dia do julgamento final.
</p>

<h3 class="no-margin-bottom">VISÃO</h3>
<span class="sub-title no-text-indent"
	>(Eclesiastes 4:12) <i>“Um cordão de três dobras não se rompe facilmente”.</i></span
>
<p>
	Afirmar-se no mundo como Igreja da UNIDADE. UNIDADE entre todas a Igrejas do Senhor Jesus Cristo <u>na terra</u>.
	UNIDADE ininterrupta, como o cordão de três dobras; e, assim com forças redobradas, obter a UNIÃO entre as famílias da
	COMUNIDADE, incentivando a empatia em tempos onde o amor já vem se esfriando.
</p>

<h3 class="no-margin-bottom">VALORES</h3>
<span class="sub-title no-text-indent">(Salmos 51:10-13)</span>
