<h2>2ª Igreja subSEDE – Igreja da EXPANSÃO</h2>
<p>
	Acima a porta de entrada para a Igreja SEDE – Igreja PIONEIRA, <b>já a 2ª. Igreja subSEDE</b> – Igreja da
	<b>EXPANSÃO</b>, vem após a autora proceder a INSERÇÃO da Igreja SEDE; ou seja, a Igreja SEDE uma vez INSERIDA, é quem
	cuidará da inserção da 2ª. Igreja subSEDE – Igreja da <b>EXPANSÃO</b>.
</p>
<p>
	<b>Resumindo:</b> a Igreja que desejar estar em UNIDADE neste Projeto, após haver Igreja SEDE no país, deve entrar em contato
	com a Igreja SEDE, e solicitar sua Inserção. Esta procederá a inserção passando os documentos administrativos para a administração
	desta SubSEDE. As subSEDES ocorrem a partir do segundo Estado em diante; ou seja, no Estado da Igreja SEDE, não há subSEDE
</p>

<h2>3ª Igreja Distrital <b>aqr</b> – Igreja da UNIDADE</h2>
<p>
	Já a 3ª posição da Igreja nesta “Rede de Pescar” que é a Igreja Distrital <b>aqr</b> – Igreja da UNIDADE, ocorre a
	partir do segundo Campo Missionário em diante, seja no Campo Missionário da Igreja SEDE, seja no Campo Missionário da
	Igreja subSEDE. A entrada da Igreja Distrital <b>aqr</b> – Igreja da UNIDADE, se dá por ARROLAMENTO, e a IG SEDE ou as
	IG subSEDES, tem a responsabilidade em orientar e trazer as <b>aqr</b>.<br /><br />
	<b>“aqr”</b> significa <b>“a que responde”</b> pelo Campo Missionário.
</p>
<p>
	A Igreja que sabendo da existência da IG SEDE ou IG subSEDE em seu país, e desejando vir a frente de um Campo
	Missionário pode entrar em contato em seu Estado, e solicitar seu ARROLAMENTO a partir do 2º Campo Missionário...
</p>

<h2>4ª Igreja Distrital APOIO – Igreja da UNIDADE</h2>
<p>
	Já a 4ª posição da Igreja nesta “Rede de Pescar” é a Igreja Distrital APOIO – Igreja da UNIDADE, ocorre desde o
	primeiro Campo Missionário em diante, seja no Campo Missionário da Igreja SEDE, seja no Campo Missionário da Igreja
	subSEDE.
</p>
<p>
	A entrada da Igreja Distrital APOIO – Igreja da UNIDADE, se dá por ARROLAMENTO, e a IG SEDE ou as IG subSEDES, tem a
	responsabilidade em orientar e trazer as Igrejas APOIO.<br /><br />
	<b>“APOIO”</b> significa <b>“a que apoia no Campo Missionário”</b>.
</p>

